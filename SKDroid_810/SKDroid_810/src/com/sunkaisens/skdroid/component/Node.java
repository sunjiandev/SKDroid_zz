package com.sunkaisens.skdroid.component;

import java.util.ArrayList;
import java.util.List;

/**
 * 
 * @author Dragon
 * 
 */
public class Node {
	private Node parent = null; // 父节点
	private List<Node> childrens = new ArrayList<Node>();// 子节点

	private int icon = -1; // icon(R.drawable的id)
	private boolean isChecked = false; // 是否被选中
	private boolean isExpand = true;// 是否处于扩展状态
	private boolean hasCheckBox = true;// 是否有复选框
	private boolean isVisiable = true;

	private String index = null; // GroupIndex || PersonIndex
	private String superIndex = null; // ParentIndex

	private String name = null; // Serial number for future use
	private String uri = null; // Unique
	private String displayName = null;
	private String number = null;

	private boolean isGroup;

	public Node(String index, String superIndex, String name, String uri,
			String displayName, String number, boolean isGroup, int iconId,
			boolean hasCheckBox) {
		this.index = index;
		this.superIndex = superIndex;
		this.name = name;
		this.uri = uri;
		this.displayName = displayName;
		this.number = number;
		this.isGroup = isGroup;
		this.icon = iconId;

		this.hasCheckBox = hasCheckBox;
	}

	/**
	 * 得到父节点
	 * 
	 * @return
	 * 
	 */
	public Node getParent() {
		return parent;
	}

	/**
	 * 设置父节点
	 * 
	 * @param parent
	 * 
	 */
	public void setParent(Node parent) {
		this.parent = parent;
	}

	/**
	 * 得到子节点
	 * 
	 * @return
	 * 
	 */
	public List<Node> getChildrens() {
		return childrens;
	}

	/**
	 * 是否根节点
	 * 
	 * @return
	 * 
	 */
	public boolean isRoot() {
		return parent == null ? true : false;
	}

	/**
	 * 是否隐藏图标
	 * 
	 * @return
	 * 
	 */
	public int getIcon() {
		return icon;
	}

	/**
	 * 设置图标
	 * 
	 * @param icon
	 * 
	 */
	public void setIcon(int icon) {
		this.icon = icon;
	}

	/**
	 * 是否被选中
	 * 
	 * @return
	 * 
	 */
	public boolean isChecked() {
		return isChecked;
	}

	public void setChecked(boolean isChecked) {
		this.isChecked = isChecked;
	}

	/**
	 * 是否是展开状态
	 * 
	 * @return
	 * 
	 */
	public boolean isExplaned() {
		return isExpand;
	}

	/**
	 * 设置展开状态
	 * 
	 * @param isExplaned
	 * 
	 */
	public void setExplaned(boolean isExplaned) {
		this.isExpand = isExplaned;
	}

	/**
	 * 是否有复选框
	 * 
	 * @return
	 * 
	 */
	public boolean hasCheckBox() {
		return hasCheckBox;
	}

	/**
	 * 设置是否有复选框
	 * 
	 * @param hasCheckBox
	 * 
	 */
	public void setHasCheckBox(boolean hasCheckBox) {
		this.hasCheckBox = hasCheckBox;
	}

	/**
	 * 增加一个子节点
	 * 
	 * @param node
	 * 
	 */
	public void addNode(Node node) {
		if (!childrens.contains(node)) {
			childrens.add(node);
		}
	}

	/**
	 * 移除一个子节点
	 * 
	 * @param node
	 * 
	 */
	public void removeNode(Node node) {
		if (childrens.contains(node))
			childrens.remove(node);
	}

	/**
	 * 移除指定位置的子节点
	 * 
	 * @param location
	 * 
	 */
	public void removeNode(int location) {
		childrens.remove(location);
	}

	/**
	 * 清除所有子节点
	 * 
	 */
	public void clears() {
		childrens.clear();
	}

	/**
	 * 判断给出的节点是否当前节点的父节点
	 * 
	 * @param node
	 * @return
	 * 
	 */
	public boolean isParent(Node node) {
		if (parent == null)
			return false;
		if (parent.equals(node))
			return true;
		return parent.isParent(node);
	}

	/**
	 * 递归获取当前节点级别
	 * 
	 * @return
	 * 
	 */
	public int getLevel() {
		return parent == null ? 0 : parent.getLevel() + 1;
	}

	/**
	 * 父节点是否处于折叠的状态
	 * 
	 * @return
	 * 
	 */
	public boolean isParentCollapsed() {
		if (parent == null)
			return false;
		if (!parent.isExplaned())
			return true;
		return parent.isParentCollapsed();
	}

	/**
	 * 是否叶节点（没有展开下级的几点）
	 * 
	 * @return
	 * 
	 */
	public boolean isLeaf() {
		return childrens.size() < 1 ? true : false;
	}

	public String getIndex() {
		return index;
	}

	public String getSuperIndex() {
		return superIndex;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUri() {
		return uri;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public boolean getIsGroup() {
		return isGroup;
	}

	public void setIsGroup(boolean isGroup) {
		this.isGroup = isGroup;
	}
}
